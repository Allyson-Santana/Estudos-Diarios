<?php

/**
 * Mostra o absoluto
 */
echo abs(10) . " | " . abs(-10);
echo "\n";

/**
 * Arredondamento
 */
echo round(10.4) . " | " . round(10.6);
echo "\n";

/**
 * Arredondamento para cima
 */
echo ceil(2.4) . " | " . ceil(2.0001);
echo "\n";

/**
 * Arredondamento para baixo
 */
echo floor(6.4) . " | " . floor(5.0001);
echo "\n";

/**
 * Numero aleatório
 */
echo rand(0, 100);

